def big_word(word):

    """Принимает на вход строку и возвращает ее со всеми заглавными буквами"""

    return word.upper()


def title_words(words_title):

    """Функция, которая делает заглавными первые буквы каждого слова в строке, поступившей на вход функции"""

    return words_title.title()
